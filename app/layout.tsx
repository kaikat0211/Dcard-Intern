import type { Metadata } from "next";
import Header from './components/Header'
import "./globals.css";
import StoreProvider from "./StoreProvider";
import { getUserGitHubId } from "./useractions";
import getUserData from "@/lib/user/fetchUserData";


export const metadata: Metadata = {
  title: "Dcard Intern",
  description: "Generated by create next app",
};


export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) 
{
  const isUserLoggedIn = await getUserGitHubId()
  
  if (!isUserLoggedIn) {
    return (
    <StoreProvider>
      <html lang="en">
        <body className='bg-bodycolor'>
          {children}
        </body>
      </html>
    </StoreProvider>
    )
  }
  const userInfo = await getUserGitHubId()
  let userID;
  let photo;
  if(userInfo) userID = userInfo.userId
  if(userInfo) photo = userInfo.userPhoto
  const data = await getUserData(userID)
  if (!data) {
    return (
      <html lang="en">
        <body className='bg-bodycolor'>
          <div className="text-white text-2xl">Loading...</div>
        </body>
      </html>
    )
  }
  return (
  <StoreProvider>
    <html lang="en">
        <body className='bg-bodycolor'>
          <Header profileData={data} photo={photo}/>
          {children}
        </body>
    </html>
  </StoreProvider>
  );
}
